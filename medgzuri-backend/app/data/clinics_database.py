    diagnosis_lower = diagnosis.lower()
    matched: set[str] = set()
    for keyword, specialties in SPECIALTY_ALIASES.items():
        if keyword in diagnosis_lower:
            matched.update(specialties)
    return list(matched) if matched else ["multi_specialty"]
